const _cache = {};

async function translateText(text, targetLang) {
  if (!text.trim() || targetLang === "en") return text;
  const key = targetLang + "|" + text;
  if (_cache[key]) return _cache[key];
  const stored = sessionStorage.getItem(key);
  if (stored) { _cache[key] = stored; return stored; }
  try {
    const res = await fetch(
      `https://translate.googleapis.com/translate_a/single?client=gtx&sl=auto&tl=${targetLang}&dt=t&q=${encodeURIComponent(text.slice(0, 1000))}`
    );
    const data = await res.json();
    const result = data && data[0] ? data[0].map(s => s[0]).join('') : text;
    _cache[key] = result;
    sessionStorage.setItem(key, result);
    return result;
  } catch { return text; }
}

